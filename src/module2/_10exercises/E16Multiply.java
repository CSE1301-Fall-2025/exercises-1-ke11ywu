package module2._10exercises;

public class E16Multiply {
	//Exercise 16: Write code that will print the result of
	// 10 * x, with values of x ranging from 0 to 10.
    public static void main(String[] args) {
        for (int x = 0; x <= 10; x++){
            System.out.println(10 * x);
        }
    }
}
